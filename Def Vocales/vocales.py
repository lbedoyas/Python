def es_vocal(x):
	if x == "a" or x == "e" or x == "i" or x == "o" or x == "u":
		return True
	elif x == "a" or x == "e" or x == "i" or x == "o" or x == "u":
		return True
	else:
		return False
		

resultado = es_vocal("e")
